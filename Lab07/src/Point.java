public class Point {
    private final double x;
    private final double y;
    private final int z;

    public Point(double x, double y, int z)   {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
